package simplegolangserverparser

import (
	"go/format"

	lex "github.com/medubin/gonzo/api/generate/lexer"
)

type Parser struct {
	lexer lex.Lexer
}

func NewParser(lexer lex.Lexer) Parser {
	return Parser{
		lexer: lexer,
	}
}

func (p *Parser) Parse() (string, map[string]string, error) {
	text := `// Code generated by Gonzo DO NOT EDIT.
package server
import (
	"context"

	"github.com/medubin/gonzo/api/src/cookies"
	"github.com/medubin/gonzo/api/src/handle"
	"github.com/medubin/gonzo/api/src/router"
	"github.com/medubin/gonzo/api/src/url"
)
	
	`
	fieldStarted := false
	server := server{}

	for !p.lexer.IsEOF() {
		token := p.lexer.Next()
		switch token.State {
		case lex.STATE_SERVER:
			server.ParseServer(token)
		case lex.STATE_BODY:
			server.ParseBody(token)
		case lex.STATE_RETURNS:
			server.ParseReturns(token)
		case lex.STATE_NONE:
			// we can add filtering here for illegal top levels tokens
			text += token.Chars
		case lex.REPEATED:
			switch token.Type {
			case lex.LP:
				text += "[]"
			case lex.RP:
			case lex.REPEATED:
				// do nothing
			default:
				text += token.Chars
			}
		case lex.MAP:
			switch token.Type {
			case lex.LP:
				text += "["
			case lex.COLON:
				text += "]"
			case lex.RP:
			case lex.SPACE:
				// do nothing
			default:
				text += token.Chars
			}
		case lex.STATE_TYPE:
			switch token.Type {
			default:
				text += token.Chars
			}
		case lex.STATE_FIELD:
			switch token.Type {
			case lex.LCB:
				text += " struct {"
			case lex.IDENT:
				if !fieldStarted {
					fieldStarted = true
				} else {
					text += "*"
				}
				text += token.Chars
			case lex.NEWLINE:
				fieldStarted = false
				text += token.Chars
			default:
				text += token.Chars
			}
		}
	}
	text += server.OutputTypes()

	formattedOutput, err := format.Source([]byte(text))
	if err != nil {
		return "", nil, err
	}

	endpoints := server.OutputEndpoints()

	return string(formattedOutput), endpoints, nil
}
